/**
 * @param {number[][]} matrix
 * @return {void} Do not return anything, modify matrix in-place instead.
 */
var rotate = function (matrix) {
    for (let i = 0; i <= matrix.length - 1; i++) {
        for (let j = 0; j <= matrix[i].length - 1; j++) {
            [matrix[i][j]];
        }
    }
};
